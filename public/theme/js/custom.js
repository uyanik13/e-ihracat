function LiveSearchLawyer (val) {
    const element = document.getElementById('search_result')
    if (val === '') {
      element.style.display = 'none'
    } else {
      element.style.display = 'block'
      element.innerHTML = ''
      element.innerText = 'Loading...'
      $.ajax({
        url: '/search/live-search-services',
        method: 'POST',
        data: {'_token': CSRF_TOKEN, 'search_text': val},
        success (response) {
          element.innerHTML = ''
          if (response.length < 1) {
            var new_result = '<li><a href="javascript:void(0);">No Results</a></li>'
            element.innerHTML += new_result
          }
          for (let i = 0; i < response.length; i++) {
            var new_result = `<li><a href=/service/${  response[i].url  }>${  response[i].title  }</li><hr>`
            element.innerHTML += new_result
          }
        }
      })
    }
  }

